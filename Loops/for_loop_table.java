public class for_loop_table {
    public static void main(String... s) {
        int num;
        System.out.print("Enter the number:");
        num=new java.util.Scanner(System.in).nextInt();
        for(int i=1;i<=10;i++){
             System.out.println(num+" * "+ i +" = "+ num*i);
        }
    }
}
